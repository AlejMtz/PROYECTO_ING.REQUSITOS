    private void boton_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_actualizarActionPerformed
        if (txt_nombre_cliente.getText().isEmpty() && txt_apellido_cliente.getText().isEmpty() && txt_cedula_cliente.getText().isEmpty() && txt_telefono_cliente.getText().isEmpty() && txt_direccion_cliente.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Completa todos los campos");
        } else {
            Cliente cliente = new Cliente();
            Ctrl_Cliente controlCliente = new Ctrl_Cliente();

            cliente.setNombre(txt_nombre_cliente.getText().trim());
            cliente.setApellio(txt_apellido_cliente.getText().trim());
            cliente.setCedula(txt_cedula_cliente.getText().trim());
            cliente.setTelefono(txt_telefono_cliente.getText().trim());
            cliente.setDireccion(txt_direccion_cliente.getText().trim());

            if (controlCliente.actualizar(cliente, idCliente)) {
                JOptionPane.showMessageDialog(null, "Datos del cliente actualizados");
                this.CargarTablaCliente();
                this.Limpiar();
            } else {
                JOptionPane.showMessageDialog(null, "Error al actualizar");
            }
        }

    }//GEN-LAST:event_boton_actualizarActionPerformed

    private void boton_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_eliminarActionPerformed

        Ctrl_Cliente conCliente = new Ctrl_Cliente();
        if(idCliente==0){
            JOptionPane.showMessageDialog(null, "Seleccione un cliente");
        }else{
            if(!conCliente.eliminar(idCliente)){
                JOptionPane.showMessageDialog(null, "Cliente eliminado");
                this.CargarTablaCliente();
                this.Limpiar();
            }else{
                JOptionPane.showMessageDialog(null, "Error al eliminar");
                this.Limpiar();
            }
        }
        
            
    }//GEN-LAST:event_boton_eliminarActionPerformed

