    private void EnviarDatosClienteSeleccionado(int idCliente) {
        try {
            Connection con = conexionBD.conectar();
            PreparedStatement pst = con.prepareStatement("select * from `cliente`where `id_cliente`='" + idCliente + "'");
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                txt_nombre_cliente.setText(rs.getString("nombre_cliente"));
                txt_apellido_cliente.setText(rs.getString("apellido_cliente"));
                txt_cedula_cliente.setText(rs.getString("cedula_cliente"));
                txt_telefono_cliente.setText(rs.getString("telefono_cliente"));
                txt_direccion_cliente.setText(rs.getString("direccion_cliente"));

            }
            con.close();

        } catch (SQLException e) {
            System.out.println("Error al seleccionar cliente");
        }
    }

