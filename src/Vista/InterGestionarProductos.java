    //metodo para calcular iva
    private double calcularIva(double precio, int iva) {
        int p_iva = iva;
        switch (p_iva) {
            case 0:
                IVA = 0.0;
                break;
            case 16:
                IVA = precio * 0.16;
                break;
            default:
                break;
        }
        //redondear decimales
        IVA = (double) Math.round(IVA * 100) / 100;
        return IVA;
    }

    private void EnviarDatosProductoSeleccionado(int idProducto) {
        try {
            Connection con = conexionBD.conectar();
            PreparedStatement pst = con.prepareStatement("select * from `productos`where `id_producto`='" + idProducto + "'");
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                txt_nombre_producto.setText(rs.getString("nombre_producto"));
                txt_cantidad_producto.setText(rs.getString("cantidad"));
                txt_precio_producto.setText(rs.getString("precio_producto"));
                txt_descripcion_prooducto.setText(rs.getString("descripcion_producto"));
                int iva = rs.getInt("porcentaje_iva");
                
                switch (iva) {
            case 0:
                jComboBox1_IVA.setSelectedItem("No aplica");
                break;
            case 16:
                jComboBox1_IVA.setSelectedItem("16%");
                break;
            default:
                jComboBox1_IVA.setSelectedItem("Seleccione IVA:");
                break;
        }
                int idProove = rs.getInt("id_proovedor");
                jComboBox2_proovedor.setSelectedItem(relacionarProovedores(idProove));
            }
            con.close();

        } catch (SQLException e) {
            System.out.println("Error al seleccionar producto");
        }
    }
    
